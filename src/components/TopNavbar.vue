<script>
import CreatePost from './Create/CreatePost.vue';
import Hikes from "./Hikes.vue";
import ProfileImage from "./ProfileImage.vue";
import Suggestions from "./Suggestions.vue";
export default {
  components: { Hikes, ProfileImage, Suggestions, CreatePost },
  data() {
    return {
      hikes: [],
      hikesAll: [],
      line: true,
    };
  },
  methods: {
    getHikes() {
      var myHeaders = new Headers();
      myHeaders.append("Accept", "application/json");

      var requestOptions = {
        method: "POST",
        headers: myHeaders,
        redirect: "follow",
      };

      fetch("http://localhost/Atlastrip_Backend/hike/hikesName", requestOptions)
        .then((response) => response.json())
        .then((result) => (this.hikes = result))
        .catch((error) => console.log("error", error));
    },
    getHikesAll() {
      var myHeaders = new Headers();
      myHeaders.append("Accept", "application/json");

      var requestOptions = {
        method: "POST",
        headers: myHeaders,
        redirect: "follow",
      };

      fetch(
        "http://localhost/Atlastrip_Backend/hike/getAllHikeWithCreator",
        requestOptions
      )
        .then((response) => response.json())
        .then((result) => (this.hikesAll = result))
        .catch((error) => console.log("error", error));
    },
  },
  beforeMount() {
    this.getHikes();
    this.getHikesAll();
  },
};
</script>

